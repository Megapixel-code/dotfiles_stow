local function random_drawing()
   math.randomseed(os.time())
   local drawings = {
      drawing_periodic_table(),
      drawing_montain(),
      drawing_fractal_maze(),
      drawing_desert(),
      drawing_insect(),
   }
   return drawings[math.random(#drawings)]
end

return {
   "startup-nvim/startup.nvim",
   dependencies = { "nvim-telescope/telescope.nvim", "nvim-lua/plenary.nvim", "nvim-telescope/telescope-file-browser.nvim" },
   config = function()
      require "startup".setup({
         header = random_drawing(), -- drawing section
         body = content(),          -- main section
         options = {
            mapping_keys = true,    -- display mapping

            -- if < 1 fraction of screen width
            -- if > 1 numbers of column
            cursor_column = 0.5,

            after = function() -- function that gets executed at the end
               -- <lua commands>
            end,
            empty_lines_between_mappings = true, -- add an empty line between mapping/commands
            disable_statuslines = true,          -- disable status-, buffer- and tablines
            paddings = { 1, 4 },                 -- amount of empty lines before each section (must be equal to amount of sections)
         },
         mappings = {
            execute_command = "<CR>",
            open_file = "o",
            open_file_split = "<c-o>",
            open_section = "<TAB>",
            open_help = "?",
         },
         colors = {
            background = "#1f2227",
            folded_section = "#56b6c2", -- the color of folded sections
            -- this can also be changed with the `StartupFoldedSection` highlight group
         },
         parts = { "header", "body" } -- all sections in order
      })
   end
}

